# booleans are a data type that allows us to store variables as either type 'True' or type 'False'
# you can also think of booleans as 0's and 1's, where 0 is True, and 1 is False

# define a boolean variable
the_truth = True
falsehood = False

# we can design simple decision structures based on boolean variables and their values
if the_truth == True:
    print("We know the truth!")
else:
    print("This is not true!")

if falsehood == False:
    print("We are wrong!")
else:
    print("We are not wrong!")