# addition - adds numbers
print(1 + 1) # output: 2

# subtraction - subtracts numbers
print(3 - 2) # output: 1

# multiplication - multiplies numbers
print(2 * 2) # output: 4

# (floating point) division - divides numbers (and makes decimal)
print(4 / 2) # output: 2.0

# (integer) division - divides numbers (and makes integer)
print(4 // 2) # output: 2

# modulo - divides numbers (with remainders included)
print(4 % 2) # output: 0, because 2 can go into 4 twice and have no remainders
print(4 % 1) # output: 0, because 1 can go into 4 four times and have no remainders
print(4 % 3) # output: 1, because 3 goes into 4 once and has a remainder of 1
print(1 % 4) # output: 1, because 4 cannot go into 1, so we are left with 1
print(2 % 4) # output: 2, because 4 cannot go into 2, so we are left with 2
print(3 % 4) # output: 3, because 4 cannot go into 3, so we are left with 3
print(4 % 4) # output: 0, because 4 can go into itself once and have no remainder

# exponents - raises a number to the the power of another number
print(2 ** 2) # output: 4