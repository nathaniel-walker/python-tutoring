# define variables
a = 2
b = 3
c = 4

# add a and b
print(a + b) # output: 5

# subtract b from c
print(c - b) # output: 1

# multiply a and c
print(a * c) # output: 8

# divide c by a (decimal)
print(c / a) # output: 2.0

# divide c by a (integer)
print(c // a) # output: 2

# remainder division of b on c
print(c % b) # output: 1

# raise a to the power of b
print(a ** b) # output: 8